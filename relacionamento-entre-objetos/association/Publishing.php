<?php

namespace associoation;

class Publishing
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var [type]
     */
    private $name;

    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
    }

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
    }
}
